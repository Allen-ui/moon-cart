const generateSubCategoryProducts = (
  label: string,
  category: string,
  baseItems: Array<[string, number, string]>,
  startId: number
): Product[] => {
  const modifiers = categoryModifiers[category] || defaultModifiers;
  const usedTitles = new Set(baseItems.map(([title]) => title));
  const items: Array<[string, number, string]> = [...baseItems];
  let fillIndex = 0;

  while (items.length < 50) {
    const [title, price, emoji] = baseItems[fillIndex % baseItems.length];
    const modifierIndex = Math.floor(fillIndex / baseItems.length) % modifiers.length;
    const modifier = modifiers[modifierIndex];
    const generatedTitle = `${title}${modifier}`;

    if (!usedTitles.has(generatedTitle)) {
      const priceOffset = ((fillIndex % 7) - 3) * 0.05;
      const generatedPrice = Math.max(1, Math.round(price * (1 + priceOffset) + (fillIndex % 9) * 2));
      items.push([generatedTitle, generatedPrice, emoji]);
      usedTitles.add(generatedTitle);
    }

    fillIndex++;
  }

  return items.map(([title, price, emoji], idx) => {
    const parts = title.split("·");
    const shop = parts.length > 1 ? parts[0] : undefined;
    return {
      id: startId + idx,
      title,
      price,
      image: `${label}-${idx + 1}`,
      category,
      subCategory: label,
      shop,
      sales: `${((idx + 1) * 0.7).toFixed(1)}万`,
      coupon: price > 80 ? `满${Math.floor(price / 10) * 10}减${Math.max(10, Math.floor(price * 0.18))}` : `满${Math.max(29, price)}减${Math.max(5, Math.floor(price * 0.12))}`,
      tags: tagGroups[idx % tagGroups.length],
      intro: "一份不用犹豫的虚拟奖励。加入购物车，获得下单成功的快乐，不产生任何真实消费。",
      palette: palettes[idx % palettes.length],
      emoji,
      specs: getSpecsForCategory(category),
    };
  });
};

let subCatIdCounter = 5000;
homeSubCategorySeeds.forEach(({ label, category, items }) => {
  if (items.length > 0) {
    const subProducts = generateSubCategoryProducts(label, category, items, subCatIdCounter);
    homeSubCategoryProducts.push(...subProducts);
    subCatIdCounter += subProducts.length;
  }
});

products.push(...homeSubCategoryProducts);

export const pickProducts = (category?: string, subCategory?: string) => {
  if (subCategory) {
    return products.filter((item) => item.subCategory === subCategory);
  }
  return category
    ? products.filter((item) => item.category === category)
    : products.filter((item) => item.category !== "盲盒");
};
